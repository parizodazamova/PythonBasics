# What can we do in python with numbers, number data types?
# built-in functions and operations in python with numbers
# math operations with numbers
# +, -, *, /

num1 = 56
num2 = 32

print(f"Addition of num1 and num2: {num1 + num2}")
print(f"Subtraction of num1 and num2: {num1 - num2}")
print(f"Multiplication of num1 and num2: {num1 * num2}")
print(f"Division of num1 and num2: {num1 / num2}")

print("---- new operators used in python ----")
print(f"Exponent of num1 and num2: {3**4}")
print(f"Modulo of num1 and num2: {num1 % num2}") #remainder after division
print(f"Floor of num1 and num2: {num1 // num2}")

